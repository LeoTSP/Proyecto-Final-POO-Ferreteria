    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        txtNombre.setEnabled(true);
        txtEdad.setEnabled(true);
        txtSexo.setEnabled(true);
        txtTelefono.setEnabled(true);
        txtDireccion.setEnabled(true);
        txtClave.setEnabled(true);
        btnNuevo.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnGuardar.setEnabled(false);
        btnActualizar.setEnabled(true);
        btnCancelar.setEnabled(true);

        valor = JOptionPane.showInputDialog("Ingrese el ID del cliente que desea editar");

        InstruccionesClientes objBuscar = new InstruccionesClientes();
        ResultSet buscarD = objBuscar.BuscarClientes(valor);
        if (buscarD == null) {
            JOptionPane.showMessageDialog(null, "No existen registros");
        }
        if (valor != null) {
            try {
                InstruccionesClientes objClientes = new InstruccionesClientes();
                misRegistros = objClientes.BuscarClientes(valor);
                misRegistros.next();
                txtNombre.setText(misRegistros.getString(1));
                txtEdad.setText(misRegistros.getString(2));
                txtSexo.setText(misRegistros.getString(3));
                txtTelefono.setText(misRegistros.getString(4));
                txtDireccion.setText(misRegistros.getString(5));
                txtClave.setText(misRegistros.getString(6));
            } catch (Exception er) {
                JOptionPane.showMessageDialog(null, "Ingrese el ID a buscar", "Error", JOptionPane.ERROR_MESSAGE);
                txtNombre.setText("");
                txtEdad.setText("");
                txtSexo.setText("");
                txtTelefono.setText("");
                txtDireccion.setText("");
                txtClave.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se ingreso un ID");
        }
    }//GEN-LAST:event_btnEditarActionPerformed
